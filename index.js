async function getActiveOrders(userId) {
    const { data, error } = await supabase
        .from('orders')
        .select('id, status, created_at')
        .eq('user_id', userId)
        .neq('status', '–≤—ã–ø–æ–ª–Ω–µ–Ω–æ')
        .order('created_at', { ascending: false });

    return { data, error };
}

